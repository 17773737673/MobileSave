package com.example.reviewmobile.engine;

import android.graphics.Bitmap;
import android.graphics.drawable.Drawable;

public class AppInfo {
	private Drawable icon; //app图标
	private String apkName;//app名称
	private String apkSize; //app大小
	private boolean userApk;//用户app与系统app
	private boolean isRom;	//放置位置
	private String apkPackageName;
	@Override
	public String toString() {
		return "AppInfo [icon=" + icon + ", apkName=" + apkName + ", apkSize="
				+ apkSize + ", userApk=" + userApk + ", isRom=" + isRom
				+ ", apkPackageName=" + apkPackageName + "]";
	}
	public Drawable getIcon() {
		return icon;
	}
	public void setIcon(Drawable icon) {
		this.icon = icon;
	}
	public String getApkName() {
		return apkName;
	}
	public void setApkName(String apkName) {
		this.apkName = apkName;
	}
	public String getApkSize() {
		return apkSize;
	}
	public void setApkSize(String apkSize) {
		this.apkSize = apkSize;
	}
	public boolean isUserApk() {
		return userApk;
	}
	public void setUserApk(boolean userApk) {
		this.userApk = userApk;
	}
	public boolean isRom() {
		return isRom;
	}
	public void setRom(boolean isRom) {
		this.isRom = isRom;
	}
	public String getApkPackageName() {
		return apkPackageName;
	}
	public void setApkPackageName(String apkPackageName) {
		this.apkPackageName = apkPackageName;
	}
	
	
}
